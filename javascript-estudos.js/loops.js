// Contador de 1 até 10

// Define o valor inicial do contador
let contador = 1;

// Repete enquanto o contador for menor ou igual a 10
while (contador <= 10) {

    // Mostra o valor atual no console
    console.log (contador)

    // aumenta 1 do contador
    contador ++;
}

// ===============================

// Contador de 10 até 0

// Define o valor inicial do contador
let contador = 10;

// Repete enquanto o contador for maior ou igual a 0
while (contador >= 0) {

    // Mostra o valor atual no console
    console.log (contador)
    
    // Diminui 1 do contador
    contador --;
}

