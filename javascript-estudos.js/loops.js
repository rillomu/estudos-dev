// Contagem regressiva

// Pede um número ao usuário
let contador = Number (prompt("Digite um numero"))

// Repete enquanto o contador for maior ou igual a 0
while (contador >= 0) {

    // Mostra o valor atual no console
    console.log (contador)
    
    // Diminui 1 do contador
    contador --;
}

